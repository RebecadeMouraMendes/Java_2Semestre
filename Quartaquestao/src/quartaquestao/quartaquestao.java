    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        char txt = texto34.getText().charAt(1);
        char txt1 = texto7.getText().charAt(1);
        if(texto1.getText().equals("") || texto5.getText().equals("") || texto3.getText().equals("") ||
                texto4.getText().equals("") || txt == ' ' || txt1 == ' ' || conta.equals("")) {
            JOptionPane.showMessageDialog(null,"Algum campo está vazio");
        }else {
            String nome, endereco, cep, cod_agencia, num_conta, telefone;
            cod_agencia = texto1.getText();
            num_conta = texto5.getText();
            nome = texto3.getText();
            endereco = texto4.getText();
            telefone = texto7.getText();
            cep = texto34.getText();
       
            //Pag2
            segundapag pag2 = new segundapag();

            pag2.setVisible(true);
            dispose();
            pag2.rotulo2.setText("<html>"+
                "Código da agência: " +cod_agencia
                +"<br>Número da conta: " +num_conta
                +"<br>Nome: "+nome
                +"<br>Endereço: " +endereco
                +"<br>Telefone: " +telefone
                +"<br>C.E.P: " +cep
                +"<br>Conta: " +conta
                +"</html>"
            );}
    }//GEN-LAST:event_jButton1ActionPerformed

